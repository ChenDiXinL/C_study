#include<stdio.h>

int main()
{
    int a[2] = {1684234849, 1751606885};
    /*
    a[0] = 100*2^(3*8)+99*2^(2*8)+98*2^(1*8)+97*2^(0*8) = 1677721600+6488064+25088+97=16842344849

    字节：   |第四字节  |第三字节  第二字节  第一字节
             |         |
    二进制值为|01100100 |01100011 |01100010 |01100001

    a[1] = 96*2^()
    
    
    */

    char *p;
    p = (char*)&a;//将每字节的二进制值提出成十进制ASCII编码，

    for(int i=0; i<8; i++)
    {
        printf("%c", *p);
        p++;
    }//abcdefgh
}